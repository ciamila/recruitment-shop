<?php

declare(strict_types=1);

namespace App\Entity;

use App\Form\Model\ProductFormModel;
use App\Repository\ProductRepository;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=ProductRepository::class)
 */
class Product
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $name;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $description;

    /**
     * @ORM\Column(type="float")
     */
    private $price;

    /**
     * @ORM\Column(type="string", length=127)
     */
    private $currency;

    /**
     * @param string $name
     * @param string $description
     * @param float  $price
     * @param string $currency
     */
    public function __construct(string $name, string $description, float $price, string $currency)
    {
        $this->name = $name;
        $this->description = $description;
        $this->price = $price;
        $this->currency = $currency;
    }

    /**
     * @return integer
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     * 
     * @return self
     */
    public function setName(string $name): self
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return string
     */
    public function getDescription(): string
    {
        return $this->description;
    }

    /**
     * @param string $description
     * 
     * @return self
     */
    public function setDescription(string $description): self
    {
        $this->description = $description;

        return $this;
    }

    /**
     * @return float
     */
    public function getPrice(): float
    {
        return $this->price;
    }

    /**
     * @param float $price
     * 
     * @return self
     */
    public function setPrice(float $price): self
    {
        $this->price = $price;

        return $this;
    }

    /**
     * @return string
     */
    public function getCurrency(): string
    {
        return $this->currency;
    }

    /**
     * @param string $currency
     * 
     * @return self
     */
    public function setCurrency(string $currency): self
    {
        $this->currency = $currency;

        return $this;
    }

    /**
     * @param ProductFormModel $productFormModel
     * 
     * @return Product
     */
    public static function createFromFormModel(ProductFormModel $productFormModel): Product
    {
        return new self(
            $productFormModel->getName(),
            $productFormModel->getDescription(),
            $productFormModel->getPrice(),
            $productFormModel->getCurrency()
        );
    }
}
